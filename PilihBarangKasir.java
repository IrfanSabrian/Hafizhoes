private void tampilData() {
    String searchText = jTextField1.getText().toLowerCase();

    // Hapus semua baris dari tabel
    model.setRowCount(0);

    try {
        String query = "SELECT * FROM barang WHERE id_sepatu LIKE '%" + searchText + 
                "%' OR merk_sepatu LIKE '%" + searchText + "%'";
        rs = perintah.executeQuery(query);

        NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID")); // Format untuk Rupiah

        while (rs.next()) {
            double hargaJual = rs.getDouble("harga_jual");
            String hargaJualFormatted = formatRupiah.format(hargaJual); // Format harga jual menjadi Rupiah
            model.addRow(new Object[]{
                rs.getString("id_sepatu"),
                rs.getString("merk_sepatu"),
                rs.getString("jenis_sepatu"),
                rs.getString("uk_sepatu"),
                rs.getString("stok"),
                hargaJualFormatted // Menampilkan harga jual dalam format Rupiah
            });
        }
    } catch (SQLException e) {
        System.err.println("Query Error");
    }
}   
    
    
