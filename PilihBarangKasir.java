    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idBarang, merekBarang, jenisBarang, ukuranBarang, stok, hargaJual;
        
        String inputValue = jTextField1.getText();
        boolean found = false;
        for (String id : dataBarang.getIdKeranjang()) {
            if (inputValue.equals(id)) {
                found = true;
                break;
            }
        }
        if (found) {
            // Kode yang dijalankan jika cocok
            JOptionPane.showMessageDialog(
                this, 
                "ID Sepatu Telah ada dalam keranjang! Pilih Barang lain!",
                "Informasi",
                JOptionPane.INFORMATION_MESSAGE
            );
        } else {
            idBarang = model.getValueAt(jTable1.getSelectedRow(), 0).toString();
            merekBarang = model.getValueAt(jTable1.getSelectedRow(), 1).toString();
            jenisBarang = model.getValueAt(jTable1.getSelectedRow(), 2).toString();
            ukuranBarang = model.getValueAt(jTable1.getSelectedRow(), 3).toString();
            stok = model.getValueAt(jTable1.getSelectedRow(), 4).toString();
            hargaJual = model.getValueAt(jTable1.getSelectedRow(), 5).toString();
            hargaJual = hargaJual.replace("Rp", "").replace(",00", "").replace(".", "");
            dataBarang.setDataBarang(idBarang, merekBarang, jenisBarang, ukuranBarang, stok, hargaJual);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

