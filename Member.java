    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // pengecekan apakah jTextField1 atau kode jurusan belum terisi
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this, // parent, biasanya this
                    "Silahkan lengkapi data ID Member!", // pesannya
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE // tipe pesan
            );
            jTextField1.requestFocus();
        } else if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this, // parent, biasanya this
                    "Silahkan lengkapi data Nama Member!", // pesannya
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE // tipe pesan   
            );
            jTextField2.requestFocus();
        } else if (jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(
                    this, // parent, biasanya this
                    "Silahkan lengkapi data No Telp!", // pesannya
                    "Informasi", // judul pesan
                    JOptionPane.INFORMATION_MESSAGE // tipe pesan   
            );
            jTextField2.requestFocus();
        } else {
            if (modeSimpan == true) { // mode insert
                dataDitemukan = cariData(jTextField1.getText());
                if (dataDitemukan == true) {
                    JOptionPane.showMessageDialog(
                            this, // parent, biasanya this
                            "Penyimpanan data Member gagal dilakukan, "
                            + "karena ID Member " + jTextField1.getText()
                            + " telah ada ditabel", // pesannya
                            "Informasi", // judul pesan
                            JOptionPane.INFORMATION_MESSAGE // tipe pesan
                    );
                } else {
                    try {
                        perintah.execute("INSERT INTO member VALUES('"
                                + jTextField1.getText() + "','"
                                + jTextField2.getText() + "','"
                                + jTextField3.getText() + "')"
                        );
                        tampilData();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(
                            this, // parent, biasanya this
                            "Penyimpanan data member gagal dilakukan", // pesannya
                            "Informasi", // judul pesan
                            JOptionPane.INFORMATION_MESSAGE // tipe pesan
                        );
                    }
                }
            } else { // modeSimpan = false (mode update)
                // pengecekan apakah tidak ada baris data di JTable1 yang terseleksi
                // atau apakah nilai variable dataDitemukan adalah false
                if ((jTable1.getSelectedRow() == -1) && (dataDitemukan == false)) {
                    JOptionPane.showMessageDialog(
                            this, // parent, biasanya this
                            "Silahkan pilih baris yang akan diubah!", // pesannya
                            "Informasi", // judul pesan
                            JOptionPane.INFORMATION_MESSAGE // tipe pesan
                    );
                } else {
                    // update data
                    try {
                        perintah.execute("UPDATE member SET "
                                + "nama_member = '" + jTextField2.getText() + "', "
                                + "no_telp = '" + jTextField3.getText() + "' "
                                + "WHERE id_member = '" + jTextField1.getText() + "' "
                        );
                        tampilData();
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                    }
                } 
            }
            bersihData();
            jTextField1.setEnabled(false);
            jTextField2.setEnabled(false);
            jTextField3.setEnabled(false);
            jButton1.setEnabled(true);
            jButton1.requestFocus();
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

