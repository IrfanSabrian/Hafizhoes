    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        // Pengecekan apakah tombol enter di keyboard telah ditekan
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            dataDitemukan = cariData(jTextField1.getText());
            if (dataDitemukan == true) {
                int pilihan;
                pilihan = JOptionPane.showConfirmDialog(
                        this,
                        "ID Member " + jTextField1.getText() + " sudah ada! "
                        + "Apakah anda ingin mengubah/menghapus data?",
                        "Konfirmasi",
                        JOptionPane.YES_NO_OPTION
                );
                if (pilihan == 0) { // yes
                    try {
                        jTextField2.setText(rs.getString("nama_member"));
                        jTextField3.setText(rs.getString("no_telp"));
                        jTextField1.setEnabled(false);
                        jTextField2.setEnabled(false);
                        jTextField3.setEnabled(false);
                        jButton1.setEnabled(false);
                        jButton2.setEnabled(false);
                        jButton3.setEnabled(true);
                        jButton3.requestFocus();
                        jButton4.setEnabled(true);
                        jButton5.setEnabled(true);
                    } catch (SQLException e) {
                        System.err.println("Query Error");
                    }
                } else { // no
                    jTextField1.setText("");
                    jTextField1.requestFocus();
                }
            } else { // data tidak ditemukan
                jTextField2.requestFocus();
                jTextField1.setEnabled(false);
                jTextField2.setEnabled(true);
                jTextField3.setEnabled(true);
                jButton2.setEnabled(true);
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

