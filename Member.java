    public void tampilData() {
        try {
            rs = perintah.executeQuery("SELECT * FROM member WHERE id_member != 0");
            model.setRowCount(0);
            model.fireTableDataChanged();
            while(rs.next()) { // Melakukan perulangan sebanyak jumlah data
                // data (rs) yang di hasilkan dari eksekusi perintah
                // SQL yang telah di lakukan sebelumnya
                model.addRow( // Menambah objek baris diakhir baris
                        new Object[] { // Pembuatan objek baris
                            rs.getString("id_member"),
                            rs.getString("nama_member"),
                            rs.getString("no_telp")
                        }
                );
            }
        } catch (SQLException e) {
            System.err.println("Query Error");
        }
    }
    
    private boolean cariData(String data) {
        if (!jTextField1.getText().equals("")) {
            try {
                rs = perintah.executeQuery("SELECT * FROM member " + 
                        "WHERE id_member = '" + data + "' AND id_member != 0");
                if(rs.next()) { // data ditemukan
                    return true;
                } else { // data tidak ditemukan
                    return false;
                }
            } catch (SQLException e) {
                System.err.println("Query Error");
            }
        }
        return false;
    }
    
